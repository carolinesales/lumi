// subcomponentes

function SectionCard({ title, children, action }) {
  return (
    <section className="flex flex-col gap-5 rounded-[16px] bg-surface p-6">
      {(title || action) && (
        <div className="flex items-center justify-between gap-3">
          {title && <h2 className="font-['Montserrat'] text-lg font-semibold text-text">{title}</h2>}
          {action}
        </div>
      )}
      {children}
    </section>
  )
}

function Field({ label, value, onChange, type = 'text', placeholder, hint, disabled }) {
  return (
    <div className="flex flex-col gap-1.5">
      <label className="font-['Nunito_Sans'] text-xs font-semibold text-text-secondary">{label}</label>
      <Input
        type={type}
        value={value}
        onChange={e => onChange?.(e.target.value)}
        placeholder={placeholder}
        disabled={disabled}
        className="py-3 text-sm"
      />
      {hint && <p className="font-['Nunito_Sans'] text-[11px] leading-relaxed text-text-tertiary">{hint}</p>}
    </div>
  )
}

function Segmented({ options, value, onChange }) {
  return (
    <div className="flex items-center rounded-full bg-surface-subtle p-[3px]">
      {options.map(opt => {
        const ativo = value === opt.value
        return (
          <button key={opt.value} type="button" onClick={() => onChange(opt.value)} aria-pressed={ativo}
            className={cn(
              "flex-1 rounded-full py-2 font-['Nunito_Sans'] text-sm font-semibold transition",
              ativo ? 'bg-ink text-white shadow-sm' : 'text-text-tertiary hover:text-text-secondary',
            )}>
            {opt.label}
          </button>
        )
      })}
    </div>
  )
}

function Toggle({ title, desc, value, onChange }) {
  return (
    <div className="flex items-center gap-4">
      <div className="flex flex-1 flex-col gap-1">
        <p className="font-['Montserrat'] text-sm font-semibold text-text">{title}</p>
        {desc && <p className="font-['Nunito_Sans'] text-xs leading-5 text-text-secondary">{desc}</p>}
      </div>
      <button type="button" role="switch" aria-checked={value} onClick={() => onChange(!value)}
        className={cn('relative h-6 w-11 shrink-0 rounded-full transition-colors', value ? 'bg-ink' : 'bg-paper-300')}>
        <span className={cn('absolute top-0.5 h-5 w-5 rounded-full bg-white shadow-sm transition-all', value ? 'left-[22px]' : 'left-0.5')} />
      </button>
    </div>
  )
}

